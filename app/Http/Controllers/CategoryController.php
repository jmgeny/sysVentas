    public function index(Request $request)
    {
        if ($request) {
            $query=trim($request->get('searchText'));
            $categorias=Category::where('nombre','LIKE','%'.$query.'%')
                        ->where('status','=','1')
                        ->orderBy('id','desc')
                        ->paginate(6);
            return view('almacen.category.index',['categorias' => $categorias, 
